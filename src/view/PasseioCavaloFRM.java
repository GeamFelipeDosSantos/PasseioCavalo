    /**
     * Chama o método 'passear' da classe cavalo, que por sua vez executa os
     * processos necessários para resolução do problema do passeio do cavalo.
     */
    private void executarPasseioCavalo() {

        if (verificarLinhaColunaInformadaValida()) {

            this.mudarVisibilidadeLabelsTabuleiro(false);
            this.mudarVisibilidadeLabelsResultado(false);

            Cavalo.passear(Integer.parseInt(this.jtfLinha.getText()), Integer.parseInt(this.jtfColuna.getText()));

            /*Impressão dos resultados*/
            preencherResultadosTabuleiro(Tabuleiro.tabuleiro);
            this.jlbResultadoCasasAvaliadas.setText("O número de casas analisadas foi: " + Cavalo.contagem);
            this.jlbResultadoTempoEx.setText("O código foi executado em " + (Cavalo.tempoFimPasseio - Cavalo.tempoInicioPasseio) + "ms");
            mudarVisibilidadeLabelsResultado(true);
        }else{
            JOptionPane.showMessageDialog(this, "Linha e coluna devem ter valor maior que zero e menor que nove!", "Linha/Coluna inválidas", JOptionPane.WARNING_MESSAGE);
        }

    }

    private boolean verificarLinhaColunaInformadaValida() {
        return (Integer.parseInt(this.jtfLinha.getText()) < 9
                && Integer.parseInt(this.jtfLinha.getText()) > 0
                && Integer.parseInt(this.jtfColuna.getText()) < 9
                && Integer.parseInt(this.jtfColuna.getText()) > 0);
    }

