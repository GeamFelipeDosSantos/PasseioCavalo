    /**
     * Exibe os valores da matriz do tabuleiro (No console).
     * @param tab
     * @param x
     * @param y 
     */
    public final static void imprimirTabuleiro(int[][] tab, int x, int y) {
        System.out.println("PASSEIO DO CAVALO TENDO INÍCIO");
        System.out.println("NA CASA (" + (x + 1) + ", " + (y + 1) + ")");
        System.out.println("-------------------------------");
        for (int i = 0; i < Tabuleiro.DIMENSAO_TABULEIRO; i++) {
            for (int j = 0; j < Tabuleiro.DIMENSAO_TABULEIRO; j++) {
                if (tab[i][j] < 10) {
                    System.out.print(" ");
                }
                System.out.print(" ");
                System.out.print(tab[i][j]);
                System.out.print(" ");
            }
            System.out.println();
        }
    }

